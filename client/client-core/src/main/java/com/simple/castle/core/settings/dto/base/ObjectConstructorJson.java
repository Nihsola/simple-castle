package com.simple.castle.core.settings.dto.base;

public class ObjectConstructorJson {
    private String nodesPattern;
    private String shape;
    private Float mass;

    public String getNodesPattern() {
        return nodesPattern;
    }

    public void setNodesPattern(String nodesPattern) {
        this.nodesPattern = nodesPattern;
    }

    public String getShape() {
        return shape;
    }

    public void setShape(String shape) {
        this.shape = shape;
    }

    public Float getMass() {
        return mass;
    }

    public void setMass(Float mass) {
        this.mass = mass;
    }
}
